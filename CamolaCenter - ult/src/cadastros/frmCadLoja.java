    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        // TODO add your handling code here:
        if (btnSalvar.isEnabled() != false) {

            if (txtRazSocial.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o Campo Razão Social!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtRazSocial.grabFocus();
            } else if (txtFantasia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o Campo Fantasia!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtFantasia.grabFocus();
            } else if (txtInscrEst.getText().equals("___.___.___-____")) {
                JOptionPane.showMessageDialog(this, "Preencha o Campo Inscrição Estadual!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtInscrEst.grabFocus();
            } else if (txtCnpj.getText().equals("__.___.___/____-__")) {
                JOptionPane.showMessageDialog(this, "Preencha o Campo CNPJ!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtCnpj.grabFocus();
            } else if (txtGerLoja.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o Campo com o nome do Gerente!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtGerLoja.grabFocus();
            } else if (txtEmailCont.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha o Campo com o Email do Gerente!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtEmailCont.grabFocus();
            } else if (!(txtEmailCont.getText().toLowerCase().matches(".+@.+\\.[a-z]+"))) {
                JOptionPane.showMessageDialog(this, "Digite um formato de email de válido!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtEmailCont.grabFocus();
            } else if (txtTelCont.getText().equals("(__)________")) {
                JOptionPane.showMessageDialog(this, "Preencha o campo telefone!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtTelCont.grabFocus();
            } else if (txtCelCont.getText().equals("(__)________")) {
                JOptionPane.showMessageDialog(this, "Preencha o campo celular!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
                txtCelCont.grabFocus();

            } else {


                try {
                    clsFunctions function = new clsFunctions();

                    boolean bStatus = optAtivo.isSelected();
                    System.out.print(bStatus);
                    int iStatus;
                    if (bStatus == true) {
                        iStatus = 1;
                    } else {
                        iStatus = 0;
                    }
                    System.out.print(iStatus);


                    clsCadLoja CadLoja = new clsCadLoja(Integer.parseInt(txtCodLoja.getText()),
                            txtFantasia.getText(),
                            txtCnpj.getText(),
                            txtRazSocial.getText(),
                            txtInscrEst.getText(),
                            txtMissao.getText(),
                            txtObserv.getText(),
                            txtGerLoja.getText(),
                            txtTelCont.getText(),
                            txtCelCont.getText(),
                            txtEmailCont.getText().toLowerCase(),
                            txtDataCad.getText(),
                            iStatus);

                    clsCadLojaDAO CadLojaDAO = new clsCadLojaDAO();

                    CadLojaDAO.InserirLoja(CadLoja);

                    clsFerramentaLoja fLoja = new clsFerramentaLoja();
                    fLoja.HabilitaNovo(txtCodLoja, txtRazSocial, txtFantasia, txtInscrEst, txtCnpj, txtMissao, optAtivo, optInativo,
                            txtGerLoja, txtEmailCont, txtTelCont, txtCelCont, txtObserv, lblStatus);
                    function.carregaTabela(CadLojaDAO.ListaLoja(), tblLoja);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarMouseClicked
