    private void cboChamadoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboChamadoItemStateChanged
        // TODO add your handling code here:

        if (!WinOppening) {
            if (cboChamado.getSelectedIndex() != 0) {

                btnAlterar.setEnabled(true);

                try {

                    clsFinalizarChamadoDAO FimChamadoDAO = new clsFinalizarChamadoDAO();
                    clsFunctions function = new clsFunctions();

                    String sChamado;
                    int iChamado, end;

                    sChamado = (String) cboChamado.getSelectedItem();

                    end = sChamado.indexOf(" ");

                    if (end == -1) {
                        iChamado = Integer.parseInt(sChamado);
                    } else {
                        iChamado = Integer.parseInt(sChamado.substring(0, end));
                    }

                    ResultSet rst = FimChamadoDAO.ConsultarFimChamado(iChamado);

                    if (rst.next()) {
                        txtSolucao.setText(rst.getString("SOLUCAO"));
                        txtData.setDate(rst.getDate("FECHA"));
                    } else {
                        txtSolucao.setText("");
                        txtData.setDate(null);
                    }

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this,
                            ex.getMessage() + ".\n", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                btnAlterar.setEnabled(false);
            }

        }

    }//GEN-LAST:event_cboChamadoItemStateChanged
