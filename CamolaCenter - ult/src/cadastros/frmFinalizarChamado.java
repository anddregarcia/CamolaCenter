    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:

        if (btnSalvar.isEnabled() != false) {

            if (cboChamado.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha um chamado!!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
            }
            else if (txtData.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Escolha uma data!!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
            } else {

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String sData = sdf.format(txtData.getDate()); // pego e transformo a data no padrão acima

                String sChamado;
                int iChamado, end;

                sChamado = (String) cboChamado.getSelectedItem();
                end = sChamado.indexOf(" ");

                if (end == -1) {
                    iChamado = Integer.parseInt(sChamado);
                } else {
                    iChamado = Integer.parseInt(sChamado.substring(0, end));
                }

                try {

                    clsFinalizarChamado FimChamado = new clsFinalizarChamado(iChamado, txtSolucao.getText(), sData);
                    clsFerramentaFimChamado fChamado = new clsFerramentaFimChamado();
                    clsFinalizarChamadoDAO FimChamadoDAO = new clsFinalizarChamadoDAO();
                    clsFunctions function = new clsFunctions();

                    FimChamadoDAO.FecharChamado(FimChamado);

                    fChamado.HabilitaNovo(cboChamado, txtData, txtSolucao, btnAlterar, btnSalvar);

                    function.carregaTabela(FimChamadoDAO.ListaFimChamado(), tblChamado);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
}//GEN-LAST:event_btnSalvarMouseClicked

