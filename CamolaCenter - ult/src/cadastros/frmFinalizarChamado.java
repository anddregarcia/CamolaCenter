    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        /*Para usar a classe de funções é necessário adicionar uma Exceção, para que se der alguma erro
         * o mesmo possa ser tratado.
         * Como não é possível alterar a linha que declara este método foi necessário inserir o função Try Catch
         */
        WinOppening = true;

        try {

            clsFunctions function = new clsFunctions();
            clsFinalizarChamadoDAO FinalizaChamadoDAO = new clsFinalizarChamadoDAO();

            clsFerramentaFimChamado fChamado = new clsFerramentaFimChamado();

            fChamado.HabilitaNovo(cboChamado, txtData, txtSolucao, btnAlterar, btnSalvar);

            function.carregaTabela(FinalizaChamadoDAO.ListaFimChamado(), tblChamado); //carrega a tabela de turno ao abrir tela

            cboChamado.removeAllItems();

            function.carregaCombo(FinalizaChamadoDAO.CarregaComboChamado(), cboChamado, "CHAMADO");

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreram problemas ao abrir tela.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }

        WinOppening = false;

    }//GEN-LAST:event_formWindowOpened

