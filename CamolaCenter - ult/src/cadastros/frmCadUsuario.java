    private void txtCodUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodUsuarioKeyPressed
        // TODO add your handling code here:
        try {
            clsCadUsuarioDAO CadUsuarioDao = new clsCadUsuarioDAO();
            clsFunctions function = new clsFunctions();

            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

                ResultSet rst = CadUsuarioDao.ConsultarUsuario(Integer.parseInt(txtCodUsuario.getText()));

                rst.next();

                txtLogin.setText(rst.getString("LOGIN"));
                txtSenha.setText(rst.getString("SENHA"));
                txtConfirmacao.setText(rst.getString("SENHA"));
                //txtDataCad.setText(rst.getString("CADASTRATO"));
                cboTpAcesso.setSelectedIndex(rst.getInt("TIPO_USUARIO") - 1); //o índice começa do ZERO


                btnAlterar.setEnabled(true);
            } 
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Usuario não cadastrado.", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
            txtCodUsuario.setText(null);
        }
    }//GEN-LAST:event_txtCodUsuarioKeyPressed

