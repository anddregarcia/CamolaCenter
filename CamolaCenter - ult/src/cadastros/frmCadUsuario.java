    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        /*Para usar a classe de funções é necessário adicionar uma Exceção, para que se der alguma erro
         * o mesmo possa ser tratado.
         * Como não é possível alterar a linha que declara este método foi necessário inserir o função Try Catch
         */

        try {
            clsFunctions function = new clsFunctions();
            clsCadUsuarioDAO CadUsuarioDao = new clsCadUsuarioDAO();

            clsFerramentaUsuario fUsuario = new clsFerramentaUsuario();
            fUsuario.HabilitarNovo(txtCodUsuario, txtLogin, txtSenha, txtConfirmacao, cboTpAcesso, btnAlterar, btnSalvar);

            function.dataSistema(txtDataCad);

            cboTpAcesso.addItem("1 - Administrativo");
            cboTpAcesso.addItem("2 - Gerencial");
            cboTpAcesso.setSelectedIndex(0);

            function.carregaTabela(CadUsuarioDao.ListaUsuario(), tblUsuario); //carrega a tabela de Usuario ao abrir tela

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreram problemas ao abrir tela.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_formWindowOpened

    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        if (btnSalvar.isEnabled() == true) {

            if (txtLogin.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Preencha o Campo Login", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtLogin.grabFocus();
            } else if (txtSenha.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Preencha o Campo Senha!!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtSenha.grabFocus();
            } else if (txtSenha.getText().length() < 6) {
                JOptionPane.showMessageDialog(this,
                        "Digite uma senha com 6 ou mais Caracteres!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtSenha.grabFocus();
            } else if (!(txtSenha.getText().matches("([a-zA-Z]+)[0-9]+([a-zA-Z0-9])*"))) {
                JOptionPane.showMessageDialog(this,
                        "A senha deve conter letras e números!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtSenha.grabFocus();
            } else if (txtConfirmacao.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Confirme sua Senha", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtConfirmacao.grabFocus();
            } else if (!(txtSenha.getText().equals(txtConfirmacao.getText()))) {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível Salvar/Alterar o registro. Senhas diferentes!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // TODO add your handling code here:
                String sTpAcesso, sAux;
                int end;

                //Jogo a empresa selecionada no combo na variável sEmpresa
                sAux = (String) cboTpAcesso.getSelectedItem();

                //Pego o index do primeira ocorrência de um "-"
                end = sAux.indexOf(" ");

                //Se não encontrou o "-"...
                if (end == -1) {
                    //... É porque só tem o número da empresa
                    //Então jogo o número na variável iCodEmpr
                    sTpAcesso = sAux;
                } else {
                    //Senão uso o END para saber aonde está o número da empresa e jogo na variável iCodEmpr
                    System.out.println(sAux.substring(0, end));
                    sTpAcesso = sAux.substring(0, end);
                }

                try {



                    clsFunctions function = new clsFunctions();

                    clsCadUsuario CadUsuario = new clsCadUsuario(Integer.parseInt(txtCodUsuario.getText()),
                            txtLogin.getText(),
                            txtSenha.getText(),
                            txtDataCad.getText(),
                            sTpAcesso);

                    clsCadUsuarioDAO CadUsuarioDao = new clsCadUsuarioDAO();

                    CadUsuarioDao.InserirUsuario(CadUsuario);

                    clsFerramentaUsuario fUsuario = new clsFerramentaUsuario();
                    fUsuario.HabilitarNovo(txtCodUsuario, txtLogin, txtSenha, txtConfirmacao, cboTpAcesso, btnAlterar, btnSalvar);

                    function.carregaTabela(CadUsuarioDao.ListaUsuario(), tblUsuario); //atualiza a tabela de Usuario ao salvar registro


                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:
        try {
            clsFunctions function = new clsFunctions();

            int codigo = function.getRowTable(tblUsuario);

            clsCadUsuarioDAO CadUsuarioDao = new clsCadUsuarioDAO();

            CadUsuarioDao.ExcluiUsuario(codigo);

            clsFerramentaUsuario fUsuario = new clsFerramentaUsuario();
            fUsuario.HabilitarNovo(txtCodUsuario, txtLogin, txtSenha, txtConfirmacao, cboTpAcesso, btnAlterar, btnSalvar);

            function.carregaTabela(CadUsuarioDao.ListaUsuario(), tblUsuario); //atualiza a tabela de Usuario ao excluir registro

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Não foi possível realizar Exclusão.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirMouseClicked

