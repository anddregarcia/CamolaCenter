    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        /*Para usar a classe de funções é necessário adicionar uma Exceção, para que se der alguma erro
         * o mesmo possa ser tratado.
         * Como não é possível alterar a linha que declara este método foi necessário inserir o função Try Catch
         */

        try {
            clsFunctions function = new clsFunctions();
            clsCadContratoDAO cadContratoDAO = new clsCadContratoDAO();

            txtCodContrato.setText(Integer.toString(function.ProxRegistro("CC_CAD_CONTRATO", "ID_CONTRATO")));

            function.carregaCombo(cadContratoDAO.ListaComboLoja(), cboLoja, "LOJA");
            function.carregaCombo(cadContratoDAO.ListaComboSetor(), cboSetor, "SETOR");

            function.carregaTabela(cadContratoDAO.ListaContrato(), tblContrato); //carrega a tabela de Contrato ao abrir tela

            clsFerramentaContrato fContrato = new clsFerramentaContrato();
            fContrato.HabilitaNovo(txtCodContrato, cboLoja, cboSetor, txtRespLoja, txtRespShop, txtDataIni, txtDataFim, txtVlrParcela, txtDiaPgto, btnAlterar, btnSalvar, btnExcluir);

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreram problemas ao abrir tela.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_formWindowOpened

