    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:

        String sMsg = "";

        if (btnSalvar.isEnabled() != false) {
            if (txtDataIni.getDate() == null) {
                sMsg = "- Escolha uma data inicial!\n";
            }
            if (txtDataFim.getDate() == null) {
                sMsg = sMsg + "- Escolha uma data fim!\n";
            }
            if (cboLoja.getSelectedIndex() == 0) {
                sMsg = sMsg + "- Escolha a Loja!\n";
            }
            if (cboSetor.getSelectedIndex() == 0) {
                sMsg = sMsg + "- Escolha o Setor!";
            }

            if (!sMsg.equals("")) {

                JOptionPane.showMessageDialog(this,
                        "Mensagem:\n"
                        + sMsg, "Atenção!", JOptionPane.ERROR_MESSAGE);
                
            } else {

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String sDataIni = sdf.format(txtDataIni.getDate()); // pego e transformo a data no padrão acima
                String sDataFim = sdf.format(txtDataIni.getDate()); // pego e transformo a data no padrão acima

                String sLoja, sSetor;
                int iLoja, iSetor, end;

                sLoja = (String) cboLoja.getSelectedItem();
                end = sLoja.indexOf(" ");

                if (end == -1) {
                    iLoja = Integer.parseInt(sLoja);
                } else {
                    iLoja = Integer.parseInt(sLoja.substring(0, end));
                }
                sSetor = (String) cboSetor.getSelectedItem();
                end = sSetor.indexOf(" ");

                if (end == -1) {
                    iSetor = Integer.parseInt(sSetor);
                } else {
                    iSetor = Integer.parseInt(sSetor.substring(0, end));
                }

                try {

                    clsCadContrato CadContrato = new clsCadContrato(sDataIni, sDataFim, txtRespLoja.getText(), txtRespShop.getText(),
                            Float.parseFloat(txtVlrParcela.getText()), Integer.parseInt(txtCodContrato.getText()), iLoja, iSetor, Integer.parseInt(txtDiaPgto.getText()));

                    clsCadContratoDAO CadContratoDAO = new clsCadContratoDAO();

                    CadContratoDAO.InserirContrato(CadContrato);

                    clsFerramentaContrato fChamado = new clsFerramentaContrato();
                    fChamado.HabilitaNovo(txtCodContrato, cboLoja, cboSetor, txtRespLoja, txtRespShop, txtDataIni, txtDataFim, txtDiaPgto, txtVlrParcela, btnAlterar, btnSalvar, btnExcluir);

                    clsFunctions function = new clsFunctions();
                    function.carregaTabela(CadContratoDAO.ListaContrato(), tblContrato);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
}//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:
        if (btnExcluir.isEnabled() == true) {
            try {
                clsFunctions function = new clsFunctions();
                int iContrato;

                clsCadContratoDAO CadContratoDAO = new clsCadContratoDAO();

                DefaultTableModel tableModel = (DefaultTableModel) tblContrato.getModel();

                int row = tblContrato.getSelectedRow();

                if (row != -1) {//se tiver linha selecionada, exclui da linha

                    iContrato = Integer.parseInt(tableModel.getValueAt(row, 0).toString());
                    CadContratoDAO.ExcluirContrato(iContrato);

                } else if (!txtCodContrato.getText().isEmpty()) { //senão exclui do campo
                    CadContratoDAO.ExcluirContrato(Integer.parseInt(txtCodContrato.getText()));

                } else {
                    JOptionPane.showMessageDialog(this,
                            "Selecione o dado na tabela ou digite um código a ser excluído.\n", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }

                clsFerramentaContrato fContrato = new clsFerramentaContrato();
                fContrato.LimparContrato(txtCodContrato, cboLoja, cboSetor, txtRespLoja, txtRespShop, txtDataIni, txtDataFim, txtDiaPgto, txtVlrParcela);

                function.carregaTabela(CadContratoDAO.ListaContrato(), tblContrato); //atualiza a tabela de Contrato ao excluir registro

            } catch (Exception err) {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível realizar Exlusão.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_btnExcluirMouseClicked

