    private void txtCodContratoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodContratoKeyPressed
        // TODO add your handling code here:


        try {
            clsCadContratoDAO cadContratoDAO = new clsCadContratoDAO();
            clsFerramentaContrato fContrato = new clsFerramentaContrato();

            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

                ResultSet rst = cadContratoDAO.ConsultarContrato(Integer.parseInt(txtCodContrato.getText()));
                if (rst.next()) {

                    cboLoja.setSelectedItem(rst.getString("LOJA"));
                    cboSetor.setSelectedItem(rst.getString("SETOR"));
                    txtRespLoja.setText(rst.getString("RESP_LOJA"));
                    txtRespShop.setText(rst.getString("RESP_SHOP"));
                    txtDataIni.setDate(rst.getDate("DATA_INICIO"));
                    txtDataFim.setDate(rst.getDate("DATA_FIM"));
                    txtDiaPgto.setText(rst.getString("DATA_MENSAL"));
                    txtVlrParcela.setText(rst.getString("VLR_PARCELA"));

                    btnAlterar.setEnabled(true);

                } else {
                    JOptionPane.showMessageDialog(this,
                            "Contrato não cadastrado.\n", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                    fContrato.LimparContrato(txtCodContrato, cboLoja, cboSetor, txtRespLoja, txtRespShop, txtDataIni, txtDataFim, txtDiaPgto, txtVlrParcela);
                    btnAlterar.setEnabled(false);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Não foi possível encontrar o contrato.\n", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txtCodContratoKeyPressed
