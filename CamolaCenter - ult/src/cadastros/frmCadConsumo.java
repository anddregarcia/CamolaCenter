    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:

        String sProduto;
        int iCodProd, end;

        sProduto = (String) cboProduto.getSelectedItem();

        end = sProduto.indexOf(" ");

        if (end == -1) {

            iCodProd = Integer.parseInt(sProduto);
        } else {

            System.out.println(sProduto.substring(0, end));
            iCodProd = Integer.parseInt(sProduto.substring(0, end));
        }

        try {
            clsFunctions function = new clsFunctions();

            clsCadConsumo CadConsumo = new clsCadConsumo(txtDataConsumo.getText(),
                    iCodProd,
                    Double.valueOf(txtQtdConsumida.getText()).doubleValue());

            clsCadConsumoDAO CadConsDAO = new clsCadConsumoDAO();

            CadConsDAO.InserirConsumo(CadConsumo);

            txtQtdConsumida.setText("");
            txtDataConsumo.setText(function.fSysDate());
            cboProduto.setSelectedIndex(0);

            function.carregaTabela(CadConsDAO.ListaConsumo(), tblConsumo); //atualiza a tabela de empresa ao salvar registro

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "N√£o foi poss√≠vel salvar/alterar este registro.\n", "Aten√ß√£o!", JOptionPane.ERROR_MESSAGE);
        }


}//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:

        String sProduto;
        int iCodProd, end;

        sProduto = (String) cboProduto.getSelectedItem();

        end = sProduto.indexOf(" ");

        if (end == -1) {
            iCodProd = Integer.parseInt(sProduto);
        } else {
            iCodProd = Integer.parseInt(sProduto.substring(0, end));
        }

        try {

            clsFunctions function = new clsFunctions();

            clsCadConsumoDAO CadConsDAO = new clsCadConsumoDAO();

            //Cria modelo do jTable
            DefaultTableModel tableModel = (DefaultTableModel) tblConsumo.getModel();
            //Pego a linha selecionada
            int row = tblConsumo.getSelectedRow();
            //Se tiver linha selecionada
            if (row != -1) {
                //Pego o ID e a Desc do produto e jogo em sProduto
                sProduto = tableModel.getValueAt(row, 1).toString();
                //Acho o ID
                end = sProduto.indexOf(" ");

                if (end == -1) {
                    iCodProd = Integer.parseInt(sProduto);
                } else {
                    //Pego s√≥ o ID
                    iCodProd = Integer.parseInt(sProduto.substring(0, end));
                }
                //Envio a data e o ID para o m√©todo excluir o dado
                CadConsDAO.ExcluirConsumo(tableModel.getValueAt(row, 0).toString(), iCodProd);

            } else {
                CadConsDAO.ExcluirConsumo(txtDataConsumo.getText(), iCodProd);
            }

            txtQtdConsumida.setText("");
            txtDataConsumo.setText(function.fSysDate());
            cboProduto.setSelectedIndex(0);

            function.carregaTabela(CadConsDAO.ListaConsumo(), tblConsumo); //atualiza a tabela de empresa ao excluir registro

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "N√£o foi poss√≠vel realizar Exclus√£o.\n", "Aten√ß√£o!", JOptionPane.ERROR_MESSAGE);
        }
}//GEN-LAST:event_btnExcluirMouseClicked

