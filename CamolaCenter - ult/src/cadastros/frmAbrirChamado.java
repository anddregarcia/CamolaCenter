            function.carregaCombo(cadChamadoDAO.ListaComboFuncionario(), cboFunc, "FUNCIONARIO");

            function.carregaTabela(cadChamadoDAO.ListaChamado(), tblChamado); //carrega a tabela de turno ao abrir tela

