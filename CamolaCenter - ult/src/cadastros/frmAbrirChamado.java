    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:

        if (btnSalvar.isEnabled() != false) {
            if (txtData.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Escolha uma data!!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else if (cboEmpr.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha a empresa!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else if (cboFunc.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha o funcionário!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else {

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String sData = sdf.format(txtData.getDate()); // pego e transformo a data no padrão acima

                String sEmpresa, sFunc;
                int iCodEmpr, iCodFunc, end;

                sEmpresa = (String) cboEmpr.getSelectedItem();
                end = sEmpresa.indexOf(" ");

                if (end == -1) {
                    iCodEmpr = Integer.parseInt(sEmpresa);
                } else {
                    iCodEmpr = Integer.parseInt(sEmpresa.substring(0, end));
                }
                sFunc = (String) cboFunc.getSelectedItem();
                end = sFunc.indexOf(" ");

                if (end == -1) {
                    iCodFunc = Integer.parseInt(sFunc);
                } else {
                    iCodFunc = Integer.parseInt(sFunc.substring(0, end));
                }

                try {

                    clsAbrirChamado CadChamado = new clsAbrirChamado(Integer.parseInt(txtChamado.getText()),
                            txtDescricao.getText(), sData, iCodFunc, iCodEmpr);

                    clsAbrirChamadoDAO CadChamadoDAO = new clsAbrirChamadoDAO();

                    CadChamadoDAO.InserirChamado(CadChamado);

                    clsFerramentaChamado fChamado = new clsFerramentaChamado();
                    fChamado.HabilitaNovo(txtChamado, txtData, txtDescricao, cboFunc, cboEmpr, btnAlterar, btnSalvar);

                    clsFunctions function = new clsFunctions();
                    function.carregaTabela(CadChamadoDAO.ListaChamado(), tblChamado);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
}//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:
        if (btnExcluir.isEnabled() == true) {
            try {
                clsFunctions function = new clsFunctions();
                String sChamado;
                int iChamado;

                clsAbrirChamadoDAO CadChamadoDAO = new clsAbrirChamadoDAO();

                DefaultTableModel tableModel = (DefaultTableModel) tblChamado.getModel();

                int row = tblChamado.getSelectedRow();

                if (row != -1) {//se tiver linha selecionada, exclui da linha

                    sChamado = tableModel.getValueAt(row, 1).toString();
                    CadChamadoDAO.ExcluirChamado(sChamado);

                } else { //senão exclui do campo
                    CadChamadoDAO.ExcluirChamado(txtChamado.getText());
                }

                clsFerramentaChamado fChamado = new clsFerramentaChamado();
                fChamado.LimparChamado(txtChamado, txtData, txtDescricao, cboFunc, cboEmpr);

                function.carregaTabela(CadChamadoDAO.ListaChamado(), tblChamado); //atualiza a tabela de turno ao excluir registro

            } catch (Exception err) {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível realizar Exlusão.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_btnExcluirMouseClicked

