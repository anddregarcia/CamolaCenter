    private void txtChamadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtChamadoKeyPressed
        // TODO add your handling code here:
        try {

            clsAbrirChamadoDAO CadAbrirChamadoDAO = new clsAbrirChamadoDAO();
            clsFunctions function = new clsFunctions();

            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

                ResultSet rst = CadAbrirChamadoDAO.ConsultarChamado(txtChamado.getText());

                if (rst.next()) {

                    txtDescricao.setText(rst.getString("DESCRICAO"));
                    txtData.setDate(rst.getDate("ABERT"));
                    cboEmpr.setSelectedItem(rst.getString("EMPR"));
                    cboFunc.setSelectedItem(rst.getString("FUNC"));

                } else {

                    txtDescricao.setText("");
                    txtData.setDate(null);
                    cboEmpr.setSelectedItem(0);
                    cboFunc.setSelectedItem(0);

                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Funcionario não cadastrado.\n", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
            txtChamado.setText(null);
        }
    }//GEN-LAST:event_txtChamadoKeyPressed

