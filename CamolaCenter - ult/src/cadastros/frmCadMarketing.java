    private void btnSalvarProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarProdMouseClicked
        // TODO add your handling code here:
        if (btnSalvarProd.isEnabled() != false) {
            String sMsg = "";

            try {

                if (txtDesc.getText().isEmpty()) {
                    sMsg = "- Informe o produto!\n";
                }
                if (txtQtd.getText().isEmpty()) {
                    sMsg = sMsg + "- Informe a quantidade!\n";
                }
                if (txtValor.getText().isEmpty()) {
                    sMsg = sMsg + "- Informe o valor!\n";
                }

                if (!sMsg.equals("")) {

                    JOptionPane.showMessageDialog(this,
                            "Mensagem:\n"
                            + sMsg, "Atenção!", JOptionPane.ERROR_MESSAGE);

                } else {

                    int iCodProd;

                    clsFunctions function = new clsFunctions();

                    if (piProdMark == 0) {
                        iCodProd = function.ProxRegistro("CC_CAD_MARK_PROD", "ID_PROD");
                    } else {
                        iCodProd = piProdMark;
                    }

                    clsCadMarketing CadMarketing = new clsCadMarketing(Integer.parseInt(txtCodMark.getText()),
                            iCodProd,
                            txtDesc.getText(),
                            Integer.parseInt(txtQtd.getText()),
                            Float.parseFloat(txtValor.getText()));

                    clsCadMarketingDAO CadMarketingDao = new clsCadMarketingDAO();

                    //não deixar salvar produtos sem ter o marketing salvo
                    //programar um maneira de não precisar fazer isso
                    btnSalvarMouseClicked(evt);

                    CadMarketingDao.InserirProduto(CadMarketing);

                    function.carregaTabela(CadMarketingDao.ListaProdMark(Integer.parseInt(txtCodMark.getText())), tblProdNota);

                    txtDesc.setText(null);
                    txtQtd.setText(null);
                    txtValor.setText(null);
                }
            } catch (Exception ex) {

                JOptionPane.showMessageDialog(this,
                        ex.getMessage(), "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_btnSalvarProdMouseClicked

    private void txtCodMarkKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodMarkKeyPressed
        // TODO add your handling code here:
        try {

            clsCadMarketingDAO CadMarketingDao = new clsCadMarketingDAO();
            clsFunctions function = new clsFunctions();

            if (evt.getKeyCode() == evt.VK_ENTER) {

                System.out.println(txtNotaFiscal.getText());

                ResultSet rst = CadMarketingDao.ConsultarMarketing(Integer.parseInt(txtCodMark.getText()));

                if (rst.next()) {
                    txtNotaFiscal.setText(rst.getString("NOTA_FISCAL"));
                    cboEmpresa.setSelectedItem(rst.getString("EMPRESA"));
                    txtData.setDate(rst.getDate("DATA"));
                    function.carregaTabela(CadMarketingDao.ListaProdMark(Integer.parseInt(txtCodMark.getText())), tblProdNota);
                    btnAlterar.setEnabled(true);

                } else {
                    txtNotaFiscal.setText("");
                    cboEmpresa.setSelectedIndex(0);
                    txtData.setDate(null);
                    btnAlterar.setEnabled(false);
                    tblProdNota.removeAll(); //deveria remover os itens do grid
                    function.carregaTabela(CadMarketingDao.ListaProdMark(0), tblProdNota);
                }

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Marketing não cadastrado.\n", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_txtCodMarkKeyPressed

