    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:

        String sMsg = "";

        if (btnSalvar.isEnabled() != false) {
            if (txtData.getDate() == null) {
                sMsg = "- Escolha uma data!\n";
            }
            if (txtNotaFiscal.getText().isEmpty()) {
                sMsg = sMsg + "- Digite o número da nota!\n";
            }
            if (cboEmpresa.getSelectedIndex() == 0) {
                sMsg = sMsg + "- Escolha a Empresa!\n";
            }

            if (!sMsg.equals("")) {

                JOptionPane.showMessageDialog(this,
                        "Mensagem:\n"
                        + sMsg, "Atenção!", JOptionPane.ERROR_MESSAGE);

            } else {

                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String sData = sdf.format(txtData.getDate()); // pego e transformo a data no padrão acima

                String sEmpresa;
                int iEmpresa, end;

                sEmpresa = (String) cboEmpresa.getSelectedItem();
                end = sEmpresa.indexOf(" ");

                if (end == -1) {
                    iEmpresa = Integer.parseInt(sEmpresa);
                } else {
                    iEmpresa = Integer.parseInt(sEmpresa.substring(0, end));
                }

                try {
                    clsFunctions function = new clsFunctions();

                    clsCadMarketing CadMarketing = new clsCadMarketing(Integer.parseInt(txtCodMark.getText()), txtNotaFiscal.getText(), iEmpresa, sData);

                    clsCadMarketingDAO CadMaketingDAO = new clsCadMarketingDAO();

                    CadMaketingDAO.InserirMarketing(CadMarketing);

                    clsFerramentaMarketing fMarketing = new clsFerramentaMarketing();

                    function.carregaTabela(CadMaketingDAO.ListaMarketing(), tblMarketing);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
}//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:
        if (btnExcluir.isEnabled() == true) {
            try {
                clsFunctions function = new clsFunctions();
                int iMarketing = 0;

                clsCadMarketingDAO CadMarketingDAO = new clsCadMarketingDAO();

                DefaultTableModel tableModel = (DefaultTableModel) tblMarketing.getModel();

                int row = tblMarketing.getSelectedRow();

                if (row != -1) {//se tiver linha selecionada, exclui da linha

                    iMarketing = Integer.parseInt(tableModel.getValueAt(row, 0).toString());
                    CadMarketingDAO.ExcluirMarketing(iMarketing);

                } else if (!txtCodMark.getText().isEmpty()) { //senão exclui do campo
                    CadMarketingDAO.ExcluirMarketing(Integer.parseInt(txtCodMark.getText()));

                } else {
                    JOptionPane.showMessageDialog(this,
                            "Selecione o dado na tabela ou digite um código a ser excluído.\n", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }


                clsFerramentaMarketing fMarketing = new clsFerramentaMarketing();
                fMarketing.LimparTela(txtCodMark, txtNotaFiscal, cboEmpresa, txtData, txtDesc, txtQtd, txtValor, tblProdNota);

                function.carregaTabela(CadMarketingDAO.ListaMarketing(), tblMarketing); //atualiza a tabela de Contrato ao excluir registro
                function.carregaTabela(CadMarketingDAO.ListaProdMark(iMarketing), tblProdNota);

            } catch (Exception err) {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível realizar Exlusão.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_btnExcluirMouseClicked

