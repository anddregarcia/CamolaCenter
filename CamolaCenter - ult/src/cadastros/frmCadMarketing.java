    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        try {

            cboEmpresa.removeAllItems(); //remove os itens que estão no combo

            clsFunctions function = new clsFunctions();
            clsCadMarketingDAO CadMarketingDao = new clsCadMarketingDAO();
            clsFerramentaMarketing fMarketing = new clsFerramentaMarketing();

            function.carregaCombo(CadMarketingDao.ListaComboEmpresa(), cboEmpresa, "EMPRESA");

            function.carregaTabela(CadMarketingDao.ListaMarketing(), tblMarketing); //atualiza a tabela de empresa ao salvar registro

            txtCodMark.setText(Integer.toString(function.ProxRegistro("CC_CAD_MARKETING", "ID_MARKETING")));

            fMarketing.HabilitaNovo(txtCodMark, txtNotaFiscal, cboEmpresa, txtData, txtDesc, txtQtd, txtValor, tblProdNota, btnAlterar, btnSalvar, btnExcluir);
            btnNovoProd.setEnabled(true);
            btnSalvarProd.setEnabled(true);
            btnAlterarProd.setEnabled(true);
            btnExcluirProd.setEnabled(true);

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreram problemas ao abrir tela.\n", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_formWindowOpened

