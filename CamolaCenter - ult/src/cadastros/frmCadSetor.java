    private void txtCodSetorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodSetorKeyPressed
        // TODO add your handling code here:
        clsValidacao Valida = new clsValidacao();
        Valida.BlockPaste(evt);
        try {
            clsCadSetorDAO CadSetorDao = new clsCadSetorDAO();
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

                ResultSet rst = CadSetorDao.ConsultarSetor(Integer.parseInt(txtCodSetor.getText()));

                txtDesc.setText(rst.getString("DESC_LOCAL"));
                txtValor.setText(rst.getString("VLR_LOC"));
                txtTamanho.setText(rst.getString("SET_MEDIDA"));
                if (rst.getBoolean("STATUS") == true) {
                    optAtivo.setSelected(true);

                } else {
                    optInativo.setSelected(true);
                }

                btnAlterar.setEnabled(true);


            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Turno com o código: " + txtCodSetor.getText() + ",Não Cadastrado!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
            txtCodSetor.setText(null);

        }
    }//GEN-LAST:event_txtCodSetorKeyPressed

