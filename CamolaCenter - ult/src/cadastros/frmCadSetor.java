    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        /*Para usar a classe de funções é necessário adicionar uma Exceção, para que se der alguma erro
         * o mesmo possa ser tratado.
         * Como não é possível alterar a linha que declara este método foi necessário inserir o função Try Catch
         */

        try {
            clsFerramentaSetor fSetor = new clsFerramentaSetor();
            fSetor.HabilitaNovo(txtCodSetor, txtDesc, txtValor, txtTamanho, optAtivo, optInativo, btnAlterar);

            clsFunctions function = new clsFunctions();
            clsCadSetorDAO cadSetorDAO = new clsCadSetorDAO();
            function.carregaTabela(cadSetorDAO.ListaSetor(), tblSetor); //carrega a tabela de turno ao abrir tela

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Ocorreram problemas ao abrir tela.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_formWindowOpened

    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:
        if (btnSalvar.isEnabled() == true) {

            if (txtCodSetor.getText().equals("")) {
                
                JOptionPane.showMessageDialog(this,
                        "Preencha o campo código!!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtCodSetor.grabFocus();
            } else if (txtDesc.getText().equals("")) {
                JOptionPane.showMessageDialog(this,
                        "Preencha o campo com a descrição sobre o Setor!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtDesc.grabFocus();

            } else if (txtValor.getText().equals("")) {
                JOptionPane.showMessageDialog(this,
                        "Preencha o campo com o Valor do Setor!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtValor.grabFocus();
            } else if (txtTamanho.getText().equals("")) {
                JOptionPane.showMessageDialog(this,
                        "Preencha o campo com o tamanho do Setor!", "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                txtTamanho.grabFocus();
            } else {
                try {

                    boolean bStatus = optAtivo.isSelected();
                    int iStatus;
                    if (bStatus == true) {
                        iStatus = 1;
                    } else {
                        iStatus = 0;
                    }

                    clsFunctions function = new clsFunctions();

                    clsCadSetor cadSetor = new clsCadSetor(Integer.parseInt(txtCodSetor.getText()),
                            txtDesc.getText(),
                            Float.parseFloat(txtValor.getText().replace(",", ".")),
                            Float.parseFloat(txtTamanho.getText().replace(",", ".")),
                            iStatus);

                    clsCadSetorDAO cadSetorDAO = new clsCadSetorDAO();

                    cadSetorDAO.InserirSetor(cadSetor);
                    function.carregaTabela(cadSetorDAO.ListaSetor(), tblSetor);

                    clsFerramentaSetor fSetor = new clsFerramentaSetor();
                    fSetor.HabilitaNovo(txtCodSetor, txtDesc, txtValor, txtTamanho, optAtivo, optInativo, btnAlterar);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:
        try {
            clsFunctions function = new clsFunctions();
            clsCadSetorDAO cadSetorDAO = new clsCadSetorDAO();

            int iCodigo = function.getRowTable(tblSetor);
            cadSetorDAO.ExcluiSetor(iCodigo);

            clsFerramentaSetor fSetor = new clsFerramentaSetor();
            fSetor.HabilitaNovo(txtCodSetor, txtDesc, txtValor, txtTamanho, optAtivo, optInativo, btnAlterar);

            function.carregaTabela(cadSetorDAO.ListaSetor(), tblSetor); //atualiza a tabela de turno ao excluir registro

        } catch (Exception err) {
            JOptionPane.showMessageDialog(this,
                    "Não foi possível realizar Exlusão.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirMouseClicked

