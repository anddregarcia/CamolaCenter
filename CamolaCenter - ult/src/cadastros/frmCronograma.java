    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked

        // TODO add your handling code here:

        if (btnSalvar.isEnabled() != false) {
            if (cboEmpr.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha a empresa!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else if (cboFunc.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha o funcionário!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else if (cboRamo.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha o ramo!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);

            } else if (cboTurno.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Escolha o turno!",
                        "Atenção", JOptionPane.INFORMATION_MESSAGE);
            } else {

                String sEmpresa, sFunc, sTurno, sRamo;
                int iCodEmpr, iCodFunc, iCodTurno, end;

                sEmpresa = (String) cboEmpr.getSelectedItem();
                end = sEmpresa.indexOf(" ");

                if (end == -1) {
                    iCodEmpr = Integer.parseInt(sEmpresa);
                } else {
                    iCodEmpr = Integer.parseInt(sEmpresa.substring(0, end));
                }

                sFunc = (String) cboFunc.getSelectedItem();
                end = sFunc.indexOf(" ");

                if (end == -1) {
                    iCodFunc = Integer.parseInt(sFunc);
                } else {
                    iCodFunc = Integer.parseInt(sFunc.substring(0, end));
                }

                sTurno = (String) cboTurno.getSelectedItem();
                end = sTurno.indexOf(" ");

                if (end == -1) {
                    iCodTurno = Integer.parseInt(sTurno);
                } else {
                    iCodTurno = Integer.parseInt(sTurno.substring(0, end));
                }

                sRamo = (String) cboRamo.getSelectedItem();


                try {

                    clsCronograma CadCronograma = new clsCronograma(Integer.parseInt(txtCodCrono.getText()),
                            sRamo, iCodFunc, iCodEmpr, iCodTurno);

                    clsCronogramaDAO CadCronogramaDAO = new clsCronogramaDAO();

                    CadCronogramaDAO.InserirCronograma(CadCronograma);

                    clsFerramentaCronograma fCronograma = new clsFerramentaCronograma();
                    fCronograma.HabilitaNovo(txtCodCrono, cboRamo, cboFunc, cboEmpr, cboTurno, btnAlterar, btnSalvar);

                    clsFunctions function = new clsFunctions();
                    function.carregaTabela(CadCronogramaDAO.ListaCronograma(), tblCronograma);

                } catch (Exception err) {
                    JOptionPane.showMessageDialog(this,
                            "Não foi possível salvar/alterar este registro.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
}//GEN-LAST:event_btnSalvarMouseClicked

    private void btnExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnExcluirMouseClicked
        // TODO add your handling code here:
        if (btnExcluir.isEnabled() == true) {
            try {
                clsFunctions function = new clsFunctions();
                String sCronograma;

                clsCronogramaDAO CadCronogramaDAO = new clsCronogramaDAO();

                DefaultTableModel tableModel = (DefaultTableModel) tblCronograma.getModel();

                int row = tblCronograma.getSelectedRow();

                if (row != -1) {//se tiver linha selecionada, exclui da linha

                    sCronograma = tableModel.getValueAt(row, 0).toString();
                    CadCronogramaDAO.ExcluirCronograma(Integer.parseInt(sCronograma));

                } else { //senão exclui do campo
                    CadCronogramaDAO.ExcluirCronograma(Integer.parseInt(txtCodCrono.getText()));
                }

                clsFerramentaCronograma fCronograma = new clsFerramentaCronograma();
                fCronograma.LimparCronograma(txtCodCrono, cboRamo, cboFunc, cboEmpr, cboTurno);

                function.carregaTabela(CadCronogramaDAO.ListaCronograma(), tblCronograma); //atualiza a tabela de turno ao excluir registro

            } catch (Exception err) {
                JOptionPane.showMessageDialog(this,
                        "Não foi possível realizar Exlusão.\n(" + err.getMessage() + ")", "Atenção!", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_btnExcluirMouseClicked

