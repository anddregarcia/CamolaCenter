    private void cboRamoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboRamoItemStateChanged
        // TODO add your handling code here:

        try {

            clsFunctions function = new clsFunctions();
            String sTipo;
            
            clsCronogramaDAO cadCronogramaDAO = new clsCronogramaDAO();

            if (cboRamo.getSelectedIndex() == 0) {
                cboEmpr.setSelectedIndex(0);
                cboEmpr.setEnabled(false);
            } else {
                cboEmpr.setSelectedIndex(0);
                cboEmpr.setEnabled(true);

                sTipo = (String) cboRamo.getSelectedItem();

                function.carregaCombo(cadCronogramaDAO.ListaComboEmpresa(sTipo), cboEmpr, "EMPRESA");
            }
        } catch (Exception ex) {
        }

    }//GEN-LAST:event_cboRamoItemStateChanged
